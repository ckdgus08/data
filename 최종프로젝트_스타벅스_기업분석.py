"""## (4) 수익성 평가

* 당기순이익율 (매출액순이익율) = 당기순이익 / 매출액
  * 높을수록 좋음 (너무 높으면 이상함)

*자기자본수익율(ROE) = 올해 당기순이익 / 올해와 전년도의 자기자본 평균
  * 자기자본(평균) = (올해 자본총계 + 전년도 자본총계) / 2
  * 높을수록 좋음, 일정 퍼센트를 꾸준히 유지하는 것도 매우 중요
"""

# 당기순이익율

star["당기순이익율"] = star["(손익계산서)당기순이익"].div(star["(손익계산서)매출액"])
star["당기순이익율"]


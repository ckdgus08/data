"""## (3) 성장성 평가
* 매출액증가율 = (이번년도 매출액 - 전년도 매출액) / 전년도 매출액
  * 높을 수록 좋음 (단, 초기 스타트업은 비정상적으로 매출액 증가율이 높음)

* 총자산증가율 = (이번년도 총 자산 - 전년도 총자산) / 전년도 총자산
  * 높을수록 좋음

## 성장성 분석 결과 
* 성장성 우수
  * 매년 꾸준히 매출액과 총자산이 증가하고 있음.
  * 특히 매년 20% 이상되는 성장률을 매출액, 15% 이상 총 자산에서 모두 보여주고 있다.
(매출액이 그래프를 통해) 매출액이 매년 증가하고 있는 상태이며, 
증가률은 매년 20% 이상 성장하고 있다는 것을 보여줌.
(매출액 증가률 그래프를 통해 발표)

* 매출액그래프 뿐만 아니라 매출액 증가율을 본다면  2010년부터 꾸준히 약 20%이상의 증가율을 확인할 수 있다. 이에 따라 매출액도 꾸준히 증가하고 있다.  
* 총자산증가율 또한 15%이상의 증가율을 보여주고 있으며 2019년도에는 약 60%가량 대폭 증가함을 볼 수 있다. 
* 성장성은 시간이 지나면서 더욱 크게 성장한다는 것을 알 수 있다.
"""

# 매출액 그래프 

